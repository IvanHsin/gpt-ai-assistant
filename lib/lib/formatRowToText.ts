// lib/formatRowToText.ts
export function formatRowToText(row: string[]): string {
  const [
    編號, 卡號, 卡別, 憑證狀態, 持有公司名稱, 總務登記的卡號, 申請原因,
    保管單位, 保管人員編, 保管人, 使用用途, 有效日期, 是否快到期,
    用戶代碼, PiN碼, 繳回日期, 備註
  ] = row;

  return `📌 卡號：${卡號}
- 卡別：${卡別}
- 憑證狀態：${憑證狀態}
- 持有公司名稱：${持有公司名稱}
- 申請原因：${申請原因}
- 使用用途：${使用用途}
- 保管單位：${保管單位}
- 保管人：${保管人}
- 有效日期：${有效日期}
- 備註：${備註}`;
}
